package HW2;

public class Employee {
    private String fio;
    private String position;
    private String email;
    private String phoneNumber;
    private int age;

    public Employee(String fio, String position, String email, String phoneNumber, int age) {
        this.fio = fio;
        this.position = position;
        this.email = email;
        this.phoneNumber = phoneNumber;
        this.age = age;
    }

}
